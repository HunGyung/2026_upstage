"""청구 가능성 판정 로직."""
import json

from . import upstage

SYSTEM_PROMPT = """너는 보험금 청구 가능성을 판단하는 손해사정 보조 AI다.
아래 제공된 '약관 조항'을 근거로, 사용자의 진단/상황에 대해 반드시 결론을 내려야 한다.

[판단 절차]
1) 사용자의 진단/상황이 약관의 어떤 보장(담보) 항목에 해당하는지 먼저 찾는다.
   (예: '갑상선암' → '암진단비' 또는 '소액암/유사암' 담보)
2) 해당 담보의 '지급사유'를 충족하는지 본다.
3) 면책기간·감액기간·부담보 등 제약 조항이 적용되는지 본다.
4) 위를 종합해 결론을 낸다.

[가입 정보 활용]
- 사용자 가입 정보에 '가입일'과 '오늘 날짜'가 있으면, 면책기간(예: 가입 후 90일)이 이미 지났는지 직접 계산하라.
- 면책기간이 지났다면 면책을 이유로 거절하지 말고, "면책기간 90일은 경과함"이라고 명확히 밝혀라.
- 면책기간이 아직 안 지났다면 그 사실을 결론에 반영하라.

[가능성 등급 — 회피하지 말 것]
- "가능": 해당 진단이 보장 담보의 지급사유에 부합하고, 명백한 면책 사유가 없을 때.
  (면책기간 같은 '조건부' 단서는 결론을 '가능'으로 두되 주의사항에 적는다.)
- "어려움": 명백히 면책/보장제외 사유에 해당하거나, 보장하지 않는다고 약관에 명시될 때.
- "검토필요": 조항은 찾았으나 결론이 '사용자만 아는 정보'(예: 진단 시점, 가입일)에 달려 있을 때만 사용.
- "정보부족": 제공된 조항에 관련 담보가 전혀 없어 판단 근거 자체가 없을 때만 사용.
  이때는 어떤 정보/조항이 더 필요한지 '설명'에 적는다.

중요: "검토필요"나 "정보부족"으로 도망가지 마라. 조항에 보장 내용이 있으면 적극적으로 '가능/어려움'을 판단하라.

[출력 — 아래 JSON 형식만]
{
  "가능성": "가능 | 어려움 | 검토필요 | 정보부족",
  "해당담보": "매칭된 보장 담보 이름 (없으면 빈 문자열)",
  "근거조항": ["인용한 조항 제목/번호들"],
  "주의사항": ["면책기간, 감액기간 등 사용자가 확인할 점"],
  "보험료영향": "청구 시 보험료 영향 (약관에 근거 없으면 '약관만으로 확인 불가')",
  "설명": "일반인이 이해할 수 있는 한국어 결론 설명"
}

이 판단은 참고용이며 최종 확인은 보험사 또는 손해사정사를 통해야 한다."""

# 결과 형식을 안정화하기 위한 one-shot 예시
EXAMPLE_USER = """약관 조항:
[제3조(보험금의 지급사유)] 회사는 피보험자가 보험기간 중 암으로 진단확정된 경우 암진단보험금을 지급합니다.
[제5조(보험금을 지급하지 않는 사유)] 계약일부터 90일 이내 진단확정된 암은 보장하지 않습니다.

질문: 갑상선암 진단을 받았어요. 청구 가능한가요?"""

EXAMPLE_ASSISTANT = json.dumps({
    "가능성": "가능",
    "해당담보": "암진단보험금",
    "근거조항": ["제3조(보험금의 지급사유)"],
    "주의사항": ["제5조: 계약일부터 90일 이내 진단확정 시 보장 제외 — 진단 시점을 확인하세요.",
              "갑상선암은 상품에 따라 소액암으로 분류되어 감액 지급될 수 있습니다."],
    "보험료영향": "약관만으로 확인 불가",
    "설명": "암 진단 시 암진단보험금이 지급되므로 청구 가능합니다. 다만 가입 후 90일 면책기간과 소액암 감액 여부를 확인하세요.",
}, ensure_ascii=False)


def judge(question: str, clauses, user_info: str = "") -> dict:
    """질문과 관련 조항을 받아 판정 결과(dict)를 반환한다.

    user_info: 가입일·가입금액 등 약관에 없는 개인 정보(면책기간 판단 등에 활용).
    """
    if not clauses:
        return {"가능성": "정보부족", "해당담보": "", "근거조항": [], "주의사항": [],
                "보험료영향": "", "설명": "관련 약관 조항을 찾지 못했습니다. 질문을 더 구체적으로 입력해보세요."}

    context = "\n\n".join(f"[{c['조항']}]\n{c['text']}" for c in clauses)
    info = f"\n\n[사용자 가입 정보]\n{user_info}" if user_info.strip() else ""
    messages = [
        {"role": "system", "content": SYSTEM_PROMPT},
        {"role": "user", "content": EXAMPLE_USER},
        {"role": "assistant", "content": EXAMPLE_ASSISTANT},
        {"role": "user", "content": f"약관 조항:\n{context}{info}\n\n질문: {question}"},
    ]
    raw = upstage.chat(messages, json_mode=True)
    try:
        return json.loads(raw)
    except json.JSONDecodeError:
        return {"가능성": "검토필요", "해당담보": "", "근거조항": [], "주의사항": [],
                "보험료영향": "", "설명": raw}
